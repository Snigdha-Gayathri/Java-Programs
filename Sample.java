class Sample
{
    public static void main(String[] args)
{
    int i=1;//Initialisation
    while(i<=10)//Condition
    {
        System.out.print( i + " ");
        i++;//Updation
    }
    for(i=1;i<=10;i++)//Initialisation&Condition&Updation
    {
        System.out.println( i + " ");
    }
}
}